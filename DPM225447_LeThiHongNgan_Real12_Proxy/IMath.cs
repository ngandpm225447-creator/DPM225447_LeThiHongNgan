/// <summary>
/// Giao diện 'IMath' (Subject) – định nghĩa các phép toán chung
/// </summary>
public interface IMath
{
    double Add(double x, double y); // Cộng
    double Sub(double x, double y); // Trừ
    double Mul(double x, double y); // Nhân
    double Div(double x, double y); // Chia
}